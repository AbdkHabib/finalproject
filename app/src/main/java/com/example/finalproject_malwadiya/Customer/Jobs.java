package com.example.finalproject_malwadiya.Customer;

public class Jobs {
    private String name;
    private int image;
    private int count;
    private int num_staff;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getnum_staff() {
        return num_staff;
    }

    public void setnum_staff(int details) {
        this.num_staff = details;
    }
}
